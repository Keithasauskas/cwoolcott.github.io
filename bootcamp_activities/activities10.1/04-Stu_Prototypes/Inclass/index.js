// Constructor Function
function Character(name, type, age, strength, hitpoints) {
  this.name = name;
  this.type = type;
  this.age = age;
  this.strength = strength;
  this.hitpoints = hitpoints;
}

// Adds printStats method to the  Character Constructor, prints stats
Character.prototype.printStats = function () {
  console.log(
    `Name: ${this.name}\nProfession: ${this.type}\nAge: ${this.age}\nStrength: ${this.strength}\nHitPoints: ${this.hitpoints}`
  );
  console.log('\n-------------\n');
};

// if hitpoints are greating 0 then returns true otherwise false
Character.prototype.isAlive = function () {
  if (this.hitpoints > 0) {
    console.log(`${this.name} is still alive!`);
    console.log('\n-------------\n');
    return true;
  }
  console.log(`${this.name} has died!`);
  return false;
};

// takes in another object, and descreases that objects hitpoints
Character.prototype.attack = function (character2) {
  character2.hitpoints -= this.strength;
};

// Increases character stats
Character.prototype.levelUp = function () {
  this.age += 1;
  this.strength += 5;
  this.hitpoints += 25;
};

const warrior = new Character('Crusher', 'Warrior', 25, 10, 75);
const rogue = new Character('Dodger', 'Rogue', 23, 20, 50);

warrior.printStats();
rogue.printStats();

rogue.attack(warrior);

// Warriors Params; hitpoints are 55
warrior.printStats();

// Crusher is Still Available
warrior.isAlive();

rogue.levelUp();

// Dodger stats and new HitPoints 24, 25, 75
rogue.printStats();
